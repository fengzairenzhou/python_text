# mongodb 连接配置
MONHOST = '192.168.6.6'
MONPORT = 27017
MONDB = 'alice'

import pymongo
def get_porxy():
    mongoclient = pymongo.MongoClient(MONHOST,MONPORT)
    db = mongoclient[MONDB]
    proxy_list = db.host.find()
    PROXIES = []
    for proxy in proxy_list:
        PROXIES.append(proxy)
    return PROXIES

